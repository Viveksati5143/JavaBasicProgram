package Unit_01;

public class P3_Task03_Typecasting {
	
	 public static void main(String[] args){
	        //typecasting in java
	        //typecasting is converting one type of data to another type of data
	        
	        int a=65;
	        double d=a;
	        System.out.println(d);
	        System.out.println((char)a);
	        
	}

}